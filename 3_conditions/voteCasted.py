# eligible for vote

age = int(input("Enter your age: "))

if age >= 18:
    print("You are eligible to vote")
else:
    print("You are not eligible to vote")
# In this program, we are taking the age of the user as input and checking whether the user is eligible to vote or not. If the age is greater than or equal to 18, then the user is eligible to vote, otherwise not.