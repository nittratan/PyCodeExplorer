# ASCII : American Standard Code for Information Interchange 
# 7 bits (0-127)
# 128 characters
# 0-31 : Control characters
# 32-127 : printable characters
# 128-255 : Extended ASCII

# ord() : to get ASCII value of a character
# chr() : to get character of a ASCII value

# ASCII value of a character
print(ord('A'))
print(ord('a'))
print(ord('0'))

# character of a ASCII value
print(chr(65))
print(chr(97))
print(chr(48))
