# escape sequence
# non printable characters

# Escape sequences are used to represent non-printable characters in a string literal.


# \n is a newline character
print("Hello\nWorld")

# \t is a tab character
print("Hello\tWorld")

# \b is a backspace character
print("Hello\bWorld")

# \r is a carriage return character
print("Hello\rWorld")

# \f is a form feed character
print("Hello\fWorld")

# \v is a vertical tab character
print("Hello\vWorld")

# \\ is a backslash character
print("Hello\\World")

# \' is a single quote character
print("Hello\'World")

# \" is a double quote character
print("Hello\"World")

# \ooo is an octal value
print("\110\145\154\154\157")

# \xhh is a hexadecimal value
print("\x48\x65\x6c\x6c\x6f")


# \u is a unicode value
print("\u0905")
print("\u0915")

